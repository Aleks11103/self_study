# Необходимо написать программу, реализующую алгоритм написания этой песни.
# Алгоритм выводит в конце предложения следующую в алфавитном порядке букву,
# если она встречается в строке текста, а очередную строку отображает уже
# без этой буквы.

# Формат входных данных
# На вход программе подается одно слово, записанное строчными русскими
# буквами без буквы "ё".

# Формат выходных данных
# Программа должна вывести в соответствии с указанным алгоритмом строки,
# количество которых равно количеству разных букв в строке, которая получается
# путем конкатенации введенного слова и строки "запретил букву".

# Примечание 1. Текст исходной песни в первом тесте.

# Примечание 2. Поем и решаем, друзья, поем и решаем. 😂

s = input() + ' запретил букву'
lst = sorted(list(set(s)))
del lst[0]  # Удаляем пробел
while len(lst) > 0:
    print(s, lst[0])
    res = []
    for el in s:
        if el != lst[0]:
            res.append(el)
    s = ''.join(res)
    while (s.find('  ') > 0):
        s = s.replace('  ', ' ')
    s = s.strip()
    del lst[0]
