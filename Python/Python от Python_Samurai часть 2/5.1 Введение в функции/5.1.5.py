# учетом скорости и пробок. Вам нужно создать функцию road, которая принимает
# целочисленные позиционные аргументы - дистанцию(км) и скорость(км/ч). А также
# принимает позиционный аргумент - пробки(True/False). На основании этих
# аргументов функция должна рассчитать сколько времени займет прохождение пути.
# Если пробки True, то время в пути увеличивается в 2 раза. В зависимости от
# значения пробок вывести одну из 2ух следующих строк :

# "Время пути до пункта назначения без учета пробок ... ч ... мин"
# "Время пути до пункта назначения с учетом пробок ... ч ... мин"

